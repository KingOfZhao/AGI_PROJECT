# Configure Logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(name)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger("NodeSolidificationProtocol")

class NodeSolidificationError(Exception):
    """Custom exception for errors during node solidification."""
    pass

class VerificationStatus(Enum):
    SUCCESS = "VERIFIED_SUCCESS"
    FORGETTING_DETECTED = "CATASTROPHIC_FORGETTING"
    INDEX_FAILURE = "INDEX_EFFICIENCY_LOW"

@dataclass
class CognitiveNode:
    """Represents a cognitive node in the AGI memory graph."""
    node_id: str
    intent_pattern: str          # The fuzzy intent (e.g., "optimize_loop")
    code_artifact: str           # The successful Python code
    creation_timestamp: float = field(default_factory=time.time)
    utility_score: float = 1.0   # Value generated by the code
    connections: Set[str] = field(default_factory=set)

    def to_dict(self) -> Dict[str, Any]:
        return {
            "node_id": self.node_id,
            "intent": self.intent_pattern,
            "code": self.code_artifact,
            "utility": self.utility_score,
            "connections": list(self.connections)
        }

class MockGraphDatabase:
    """
    A mock in-memory Graph Database to simulate the AGI's long-term memory.
    In a real scenario, this would interface with Neo4j, NebulaGraph, or NetworkX.
    """
    def __init__(self):
        self._nodes: Dict[str, CognitiveNode] = {}
        self._intent_index: Dict[str, str] = {} # Inverted index: Intent -> NodeID
        logger.info("Mock Graph Database initialized.")

    def add_node(self, node: CognitiveNode) -> bool:
        if node.node_id in self._nodes:
            logger.warning(f"Node {node.node_id} already exists.")
            return False
        self._nodes[node.node_id] = node
        return True

    def update_index(self, intent_key: str, node_id: str) -> None:
        self._intent_index[intent_key] = node_id

    def get_node(self, node_id: str) -> Optional[CognitiveNode]:
        return self._nodes.get(node_id)

    def get_all_nodes(self) -> List[CognitiveNode]:
        return list(self._nodes.values())

    def simulate_query_latency(self, intent: str) -> float:
        """
        Simulates retrieval time. Lower is better.
        """
        # Simple simulation: constant time if exists, high penalty if graph is large
        base_time = 0.005
        size_penalty = len(self._nodes) * 0.0001
        return base_time + size_penalty

    def check_semantic_interference(self, new_node: CognitiveNode) -> float:
        """
        Checks how much the new node 'overlaps' or 'damages' existing knowledge.
        Returns a 'damage score' (0.0 to 1.0).
        """
        damage = 0.0
        for node in self._nodes.values():
            # Simplistic overlap check: if intents are too similar but code is different
            if node.intent_pattern == new_node.intent_pattern and node.node_id != new_node.node_id:
                damage += 0.5 # High collision risk
            elif node.intent_pattern in new_node.intent_pattern:
                damage += 0.1 # Minor overlap
        
        # Simulate catastrophic forgetting if graph density is too high without pruning
        if len(self._nodes) > 100:
            damage += 0.2
            
        return min(damage, 1.0)


def _generate_node_signature(intent: str, code: str) -> str:
    """
    [Helper] Generates a unique hash for the new cognitive node based on intent and code.
    """
    raw_data = f"{intent}::{code}"
    return hashlib.sha256(raw_data.encode('utf-8')).hexdigest()[:16]


def validate_indexing_efficiency(db: MockGraphDatabase, intent: str, threshold_ms: float = 50.0) -> Tuple[bool, float]:
    """
    [Core 1] Validates the retrieval speed of the newly indexed node.
    
    Args:
        db: The graph database instance.
        intent: The intent string to query.
        threshold_ms: Maximum allowed latency in milliseconds.
    
    Returns:
        Tuple[is_efficient (bool), latency (float)]
    """
    logger.info(f"Validating indexing efficiency for intent: {intent[:20]}...")
    
    # Measure retrieval time
    start_time = time.time()
    # In real implementation, this would perform a vector search or graph traversal
    _ = db._intent_index.get(intent) 
    end_time = time.time()
    
    latency_ms = (end_time - start_time) * 1000
    
    if latency_ms > threshold_ms:
        logger.warning(f"Index efficiency low: {latency_ms:.4f}ms > {threshold_ms}ms")
        return False, latency_ms
        
    logger.info(f"Index efficiency OK: {latency_ms:.4f}ms")
    return True, latency_ms


def solidify_cognitive_node(
    db: MockGraphDatabase,
    intent: str,
    code: str,
    value_score: float,
    forgetting_threshold: float = 0.7
) -> Tuple[VerificationStatus, Optional[CognitiveNode]]:
    """
    [Core 2] Main protocol function. Solidifies the mapping into a node and verifies stability.
    
    This function performs the following:
    1. Generates a new cognitive node.
    2. Checks for Catastrophic Forgetting (interference with old nodes).
    3. Commits to DB if safe.
    4. Verifies index performance post-commit.
    
    Args:
        db: The memory graph instance.
        intent: The fuzzy input intent.
        code: The successful generated code.
        value_score: The calculated value/utility of the execution.
        forgetting_threshold: Threshold for semantic interference (0.0-1.0).
        
    Returns:
        Tuple containing VerificationStatus and the created Node (or None).
    """
    logger.info("Starting Node Solidification Protocol...")
    
    # 1. Data Validation
    if not intent or not code:
        raise NodeSolidificationError("Intent and Code cannot be empty.")
    if value_score < 0:
        logger.warning("Negative value score detected, clamping to 0.")
        value_score = 0.0

    # 2. Generate Node Object
    node_id = _generate_node_signature(intent, code)
    new_node = CognitiveNode(
        node_id=node_id,
        intent_pattern=intent,
        code_artifact=code,
        utility_score=value_score
    )

    # 3. Pre-commit Validation: Catastrophic Forgetting Check
    interference = db.check_semantic_interference(new_node)
    if interference > forgetting_threshold:
        logger.error(f"Catastrophic forgetting risk detected! Interference: {interference:.2f}")
        return VerificationStatus.FORGETTING_DETECTED, None
    
    # 4. Commit to Graph Database
    try:
        success = db.add_node(new_node)
        if not success:
            raise NodeSolidificationError("Failed to write node to database.")
        
        db.update_index(intent, node_id)
        logger.info(f"Node {node_id} committed to long-term memory.")
    except Exception as e:
        logger.critical(f"Database write error: {str(e)}")
        raise NodeSolidificationError("Persistence layer failure.")

    # 5. Post-commit Validation: Index Efficiency
    is_efficient, latency = validate_indexing_efficiency(db, intent)
    if not is_efficient:
        # In a real system, we might trigger a re-indexing job here
        return VerificationStatus.INDEX_FAILURE, new_node

    return VerificationStatus.SUCCESS, new_node


