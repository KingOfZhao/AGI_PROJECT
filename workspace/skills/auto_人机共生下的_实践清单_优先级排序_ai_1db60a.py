logger = logging.getLogger(__name__)

class HypothesisCategory(Enum):
    """Categories of hypotheses based on domain."""
    THEORETICAL = "theoretical"
    EMPIRICAL = "empirical"
    HYBRID = "hybrid"

@dataclass
class Hypothesis:
    """
    Represents a single hypothesis generated by the AI.
    
    Attributes:
        id: Unique identifier for the hypothesis.
        name: Short description.
        potential_information_gain: float (0.0 to 1.0) representing the 
                                    expected value of information (EVI).
        time_cost_hours: Estimated human time required for verification.
        monetary_cost: Estimated financial cost for verification.
        dependency_skill_ids: List of SKILL node IDs required to perform verification.
        category: The type of hypothesis.
    """
    id: str
    name: str
    potential_information_gain: float
    time_cost_hours: float
    monetary_cost: float
    dependency_skill_ids: List[str] = field(default_factory=list)
    category: HypothesisCategory = HypothesisCategory.EMPIRICAL

    def __post_init__(self):
        """Validate data after initialization."""
        if not 0.0 <= self.potential_information_gain <= 1.0:
            raise ValueError(f"Invalid information gain for {self.id}: {self.potential_information_gain}")
        if self.time_cost_hours < 0:
            raise ValueError(f"Time cost cannot be negative for {self.id}")
        if self.monetary_cost < 0:
            raise ValueError(f"Monetary cost cannot be negative for {self.id}")

@dataclass
class AgentContext:
    """
    Represents the current state of the Human-AI symbiotic agent.
    
    Attributes:
        available_budget: Current available funds.
        available_time_hours: Current available time (e.g., remaining healthy lifespan hours).
        unlocked_skills: Set of currently acquired SKILL node IDs.
        time_weight: Importance weight for time (0.0 to 1.0).
        money_weight: Importance weight for money (0.0 to 1.0).
    """
    available_budget: float
    available_time_hours: float
    unlocked_skills: set
    time_weight: float = 0.7
    money_weight: float = 0.3

    def __post_init__(self):
        if self.time_weight + self.money_weight != 1.0:
            logger.warning("Weights do not sum to 1.0. Normalization recommended.")

class PriorityQueueError(Exception):
    """Custom exception for priority queue generation failures."""
    pass

def calculate_normalized_cost(hypothesis: Hypothesis, context: AgentContext) -> float:
    """
    Helper function to calculate a weighted normalized cost.
    
    Uses logarithmic scaling to handle wide variances in cost/time.
    
    Args:
        hypothesis: The hypothesis to evaluate.
        context: The current agent context.
        
    Returns:
        float: A composite cost score (lower is better).
    """
    # Avoid division by zero or log(0)
    time_val = hypothesis.time_cost_hours if hypothesis.time_cost_hours > 0 else 0.1
    money_val = hypothesis.monetary_cost if hypothesis.monetary_cost > 0 else 1.0
    
    # Simple weighted sum model (could be replaced by multi-attribute utility theory)
    weighted_cost = (context.time_weight * time_val) + (context.money_weight * (money_val / 100))
    return weighted_cost

def check_skill_dependency(hypothesis: Hypothesis, context: AgentContext) -> bool:
    """
    Checks if the necessary SKILL nodes are available in the agent's cognitive network.
    
    Args:
        hypothesis: The hypothesis to check.
        context: The current agent context.
        
    Returns:
        bool: True if all dependencies are met, False otherwise.
    """
    required = set(hypothesis.dependency_skill_ids)
    return required.issubset(context.unlocked_skills)

def generate_verification_queue(
    hypotheses: List[Hypothesis],
    context: AgentContext,
    max_results: int = 50
) -> List[Dict[str, Any]]:
    """
    Generates a dynamic verification priority queue.
    
    This function filters hypotheses based on resource constraints and skill availability,
    then ranks them based on the 'Cognitive Construction Efficiency' metric.
    
    Metric: Efficiency = Potential_Information_Gain / (Weighted_Cost + Alpha)
    Where Alpha is a small constant to prevent division by zero.
    
    Args:
        hypotheses: List of candidate hypotheses.
        context: The current state of the symbiotic agent.
        max_results: Maximum number of items to return in the queue.
        
    Returns:
        List of dictionaries containing sorted hypotheses and their scores.
        
    Raises:
        PriorityQueueError: If input data is invalid or processing fails.
    """
    if not hypotheses:
        logger.warning("Empty hypotheses list provided.")
        return []

    logger.info(f"Processing {len(hypotheses)} hypotheses for prioritization...")
    valid_candidates = []

    try:
        for h in hypotheses:
            # 1. Boundary Check: Resource Constraints
            if h.time_cost_hours > context.available_time_hours:
                continue
            if h.monetary_cost > context.available_budget:
                continue
                
            # 2. Boundary Check: Skill Dependencies (Symbiosis Constraint)
            if not check_skill_dependency(h, context):
                logger.debug(f"Skipping {h.id}: Missing required skills {h.dependency_skill_ids}")
                continue

            # 3. Calculate Efficiency Score
            cost = calculate_normalized_cost(h, context)
            if cost <= 0: cost = 0.01 # Floor for cost
            
            efficiency_score = h.potential_information_gain / cost
            
            valid_candidates.append({
                "hypothesis": h,
                "score": efficiency_score,
                "cost": cost
            })
            
    except Exception as e:
        logger.error(f"Error during hypothesis processing: {str(e)}")
        raise PriorityQueueError("Failed to process hypotheses.") from e

    # 4. Sort by Efficiency Score (Descending)
    sorted_candidates = sorted(
        valid_candidates, 
        key=lambda x: x['score'], 
        reverse=True
    )
    
    # 5. Select Top N
    final_queue = sorted_candidates[:max_results]
    
    logger.info(f"Generated priority queue with {len(final_queue)} items.")
    return final_queue

def simulate_queue_execution(
    queue: List[Dict[str, Any]], 
    context: AgentContext
) -> Dict[str, float]:
    """
    Simulates the execution of the priority queue to predict total ROI.
    
    Args:
        queue: The prioritized list of actions.
        context: The agent context.
        
    Returns:
        Dict containing total_time_consumed, total_cost, and projected_knowledge_gain.
    """
    total_time = 0.0
    total_cost = 0.0
    total_gain = 0.0
    
    logger.info("--- Starting Execution Simulation ---")
    
    for item in queue:
        h = item['hypothesis']
        
        # Check if resources run out during simulation
        if total_time + h.time_cost_hours > context.available_time_hours:
            logger.warning(f"Simulation stopped: Time budget exceeded at {h.id}")
            break
        if total_cost + h.monetary_cost > context.available_budget:
            logger.warning(f"Simulation stopped: Financial budget exceeded at {h.id}")
            break
            
        total_time += h.time_cost_hours
        total_cost += h.monetary_cost
        total_gain += h.potential_information_gain
        
        # Simulation side-effect: Potentially unlock new skills (Logic placeholder)
        # In a real AGI system, completing a hypothesis might unlock new branches.
        
    logger.info(f"Simulation Complete: Time: {total_time}h, Cost: ${total_cost}, Gain: {total_gain}")
    
    return {
        "total_time_consumed": total_time,
        "total_cost": total_cost,
        "projected_knowledge_gain": total_gain
    }

