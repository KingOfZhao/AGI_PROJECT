if __name__ == "__main__":
    # 1. Define the design parameters
    my_product_specs = DesignParameters(
        max_temp_celsius=120.0,
        max_pressure_psi=800.0,
        max_voltage_v=220.0,
        material_yield_strength=400.0
    )

    # 2. Initialize the Agent
    agent = DestructiveTestingAgent(my_product_specs)

    # 3. Run the campaign
    # We run 3 iterations of 5 cases each to find breaking points
    failure_report = agent.execute_campaign(iterations=3, batch_size=5)

    # 4. Output results
    print("\n--- Destructive Test Report ---")
    if not failure_report.empty:
        print("Detected Weak Points:")
        print(failure_report.groupby('failure_type').mean())
        print("\nRecommendation: Reinforce design based on the above average failure thresholds.")
    else:
        print("All tests passed within safe margins.")