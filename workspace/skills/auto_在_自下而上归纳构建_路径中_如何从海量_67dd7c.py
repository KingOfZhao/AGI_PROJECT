import re
import json
import logging
from typing import List, Dict, Optional, Any, Tuple
from dataclasses import dataclass, asdict, field
from enum import Enum
from datetime import datetime

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(name)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger("SkillInductor")

class SkillStatus(Enum):
    DRAFT = "draft"
    VERIFIED = "verified"
    DEPRECATED = "deprecated"

@dataclass
class ActionStep:
    """Represents a single, atomic executable step within a Skill."""
    step_id: int
    description: str
    code_snippet: Optional[str] = None
    dependencies: List[str] = field(default_factory=list)
    
    def to_dict(self) -> Dict:
        return asdict(self)

@dataclass
class Skill:
    """Represents a固化 SKILL object generated by the system."""
    skill_id: str
    name: str
    description: str
    steps: List[ActionStep]
    source_hash: str
    created_at: str
    status: SkillStatus = SkillStatus.DRAFT
    
    def to_json(self) -> str:
        return json.dumps(asdict(self), default=str, indent=2)

class UnstructuredDataError(ValueError):
    """Custom exception for invalid input data formats."""
    pass

class SkillExtractionError(Exception):
    """Custom exception for failures during the extraction process."""
    pass

def _normalize_text(text: str) -> str:
    """
    [Helper Function] Preprocesses and normalizes input text.
    Removes excessive whitespace and standardizes line endings.
    """
    if not isinstance(text, str):
        raise UnstructuredDataError("Input must be a string.")
    
    # Standardize bullet points and step markers
    normalized = text.replace('\r\n', '\n').replace('\t', ' ')
    # Remove excessive spaces
    normalized = re.sub(r' +', ' ', normalized)
    return normalized.strip()

def extract_action_sequences(raw_text: str) -> List[Tuple[int, str]]:
    """
    [Core Function 1] Parses unstructured text to identify sequential action steps.
    Uses regex patterns to detect 'Step 1', '1.', etc., or imperative sentences.
    
    Args:
        raw_text (str): The raw observation text (manuals, logs).
        
    Returns:
        List[Tuple[int, str]]: A list of tuples containing (step_index, description).
        
    Raises:
        UnstructuredDataError: If text is empty or cannot be parsed.
    """
    logger.info("Starting action sequence extraction...")
    clean_text = _normalize_text(raw_text)
    
    if len(clean_text) < 10:
        logger.warning("Input text too short for meaningful extraction.")
        return []

    # Regex for "Step 1: ...", "1. ...", "- ..."
    # This is a heuristic pattern for demonstration; production would use NLP models.
    pattern = r'(?:Step\s*(\d+)[.:\-]|\n(\d+)[.:\-]|\n\-\s*)([^\n]+)'
    matches = re.findall(pattern, clean_text, re.IGNORECASE)
    
    extracted_steps = []
    
    if matches:
        for match in matches:
            # match is a tuple (group1, group2, group3) based on regex groups
            # group1 or group2 is the number, group3 is the text
            step_num_str = match[0] or match[1]
            step_desc = match[2].strip()
            
            if step_desc:
                try:
                    step_num = int(step_num_str) if step_num_str else len(extracted_steps) + 1
                    extracted_steps.append((step_num, step_desc))
                except ValueError:
                    continue
    else:
        # Fallback: Heuristic sentence splitting for imperative sentences (Basic NLP)
        logger.info("No explicit numbering found. Attempting semantic sentence extraction.")
        sentences = re.split(r'(?<=[.!?])\s+', clean_text)
        for idx, sent in enumerate(sentences):
            # Check for imperative verbs (very naive check)
            if sent and len(sent.split()) > 2: 
                extracted_steps.append((idx + 1, sent))

    if not extracted_steps:
        logger.error("Failed to extract any actionable steps.")
        raise UnstructuredDataError("No process patterns found in text.")

    logger.info(f"Successfully extracted {len(extracted_steps)} potential steps.")
    return extracted_steps

def synthesize_skill_code(
    extracted_steps: List[Tuple[int, str]], 
    skill_name: str,
    context: Optional[Dict[str, Any]] = None
) -> Skill:
    """
    [Core Function 2] Translates extracted text steps into a structured Skill object.
    Generates a Python function skeleton or API call structure based on the text.
    
    Args:
        extracted_steps (List[Tuple[int, str]]): Steps from extract_action_sequences.
        skill_name (str): The name for the new skill.
        context (Optional[Dict]): Additional metadata (e.g., source file, author).
        
    Returns:
        Skill: The complete, structured Skill object ready for registration.
        
    Raises:
        SkillExtractionError: If skill synthesis fails.
    """
    logger.info(f"Synthesizing code for skill: {skill_name}")
    
    if not extracted_steps:
        raise SkillExtractionError("Cannot synthesize skill from empty step list.")

    action_objects: List[ActionStep] = []
    
    for idx, (step_num, desc) in enumerate(extracted_steps):
        # Generate a pseudo-code snippet or API call placeholder
        # In a real AGI system, this would interface with a Code-LLM
        func_name = desc.lower().replace(' ', '_').replace('.', '')[:20]
        code = f"# Auto-generated stub for: {desc}\ndef step_{step_num}_{func_name}():\n    pass # TODO: Implement logic"
        
        step_obj = ActionStep(
            step_id=step_num,
            description=desc,
            code_snippet=code,
            dependencies=[]  # Could be inferred in a more complex implementation
        )
        action_objects.append(step_obj)

    # Create the skill metadata
    import hashlib
    source_str = "".join([s[1] for s in extracted_steps])
    hash_id = hashlib.md5(source_str.encode()).hexdigest()[:12]
    
    new_skill = Skill(
        skill_id=f"skill_auto_{hash_id}",
        name=skill_name,
        description=f"Auto-generated skill from unstructured text: {skill_name}",
        steps=action_objects,
        source_hash=hash_id,
        created_at=datetime.utcnow().isoformat()
    )
    
    logger.info(f"Skill synthesis complete. ID: {new_skill.skill_id}")
    return new_skill

def validate_skill_integrity(skill: Skill) -> bool:
    """
    Validates the structure and integrity of the generated Skill object.
    Ensures all steps have necessary data and IDs are sequential.
    """
    if not skill.steps:
        return False
    
    ids = [s.step_id for s in skill.steps]
    # Check if IDs are strictly increasing (loose check)
    if sorted(ids) != ids:
        logger.warning(f"Skill {skill.skill_id} has non-sequential step IDs.")
        
    for step in skill.steps:
        if not step.description:
            return False
            
    return True

