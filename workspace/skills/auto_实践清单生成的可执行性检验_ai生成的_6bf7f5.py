logger = logging.getLogger(__name__)

# --- Constants and Enums ---

class ResourceType(Enum):
    HARDWARE = "hardware"
    SOFTWARE_LIBRARY = "software_library"
    OS_FEATURE = "os_feature"
    NETWORK = "network"

class FailureSeverity(Enum):
    CRITICAL = "critical"   # Execution is impossible
    WARNING = "warning"     # Execution possible but suboptimal or risky
    INFO = "info"           # Minor deviation

@dataclass
class SystemCapability:
    """Represents a specific capability of the host system."""
    name: str
    type: ResourceType
    version: Optional[str] = None
    metadata: Dict = field(default_factory=dict)

@dataclass
class ChecklistItem:
    """Represents a single item in the AI-generated practice checklist."""
    step_id: str
    description: str
    required_capabilities: List[str]  # e.g., "GPU_CUDA_11", "SKILL:1842", "Arm_Actuator"
    raw_command: Optional[str] = None

@dataclass
class ValidationResult:
    """Result of the executability validation."""
    is_executable: bool
    failures: List[Tuple[str, FailureSeverity, str]] = field(default_factory=list)
    unmet_dependencies: Set[str] = field(default_factory=set)

# --- Core Functions ---

def analyze_cross_domain_mapping(item_description: str) -> List[str]:
    """
    [Helper Function]
    Analyzes the description of a checklist item to identify implied requirements
    resulting from cross-domain analogies.
    
    Context: 
    When AI maps 'Immune System' -> 'Firewall', it might generate steps that are 
    metaphorically correct but technically ambiguous. This function extracts 
    technical requirements from natural language.
    
    Args:
        item_description (str): The natural language description of the task step.
        
    Returns:
        List[str]: A list of normalized capability strings.
    """
    # In a real AGI system, this would use NLP/Embeddings.
    # Here we use regex pattern matching for deterministic validation.
    logger.debug(f"Analyzing description: {item_description}")
    requirements = []
    
    # Pattern: Hardware requirements (e.g., GPU, Sensors)
    if re.search(r'(neural network|deep learning|inference)', item_description, re.IGNORECASE):
        requirements.append("GPU_COMPUTE")
        
    # Pattern: Legacy/Specific Skill references (e.g., SKILL:1842)
    skill_match = re.search(r'SKILL:(\d+)', item_description)
    if skill_match:
        requirements.append(f"SKILL_ID_{skill_match.group(1)}")
        
    # Pattern: Mechanical operations (Robotics context)
    if re.search(r'(grab|move|lift|actuate|physical)', item_description, re.IGNORECASE):
        requirements.append("ROBOTIC_ARM_ACTUATOR")
        
    # Pattern: Network operations
    if re.search(r'(packet|firewall|traffic|latency)', item_description, re.IGNORECASE):
        requirements.append("NETWORK_INTERFACE")
        
    return requirements

def validate_executability(
    checklist: List[ChecklistItem],
    system_profile: List[SystemCapability]
) -> ValidationResult:
    """
    [Core Function 1]
    Validates the entire checklist against the system profile.
    
    This function determines if the abstract 'Practice Checklist' can actually run 
    on the current infrastructure. It checks for the existence of specific libraries,
    hardware specs, and referenced skills (like SKILL:1842).
    
    Args:
        checklist (List[ChecklistItem]): The list of steps generated by the AI.
        system_profile (List[SystemCapability]): The current host environment capabilities.
        
    Returns:
        ValidationResult: Object containing boolean executability status and details.
    """
    logger.info(f"Starting validation for {len(checklist)} checklist items...")
    
    result = ValidationResult(is_executable=True)
    
    # Index system profile for O(1) lookup
    available_caps = {cap.name: cap for cap in system_profile}
    
    for item in checklist:
        # Extract implicit requirements from the description
        implicit_reqs = analyze_cross_domain_mapping(item.description)
        
        # Combine explicit and implicit requirements
        all_reqs = set(item.required_capabilities + implicit_reqs)
        
        for req in all_reqs:
            if req not in available_caps:
                # Missing Dependency
                result.is_executable = False
                error_msg = f"Missing dependency '{req}' for step {item.step_id}"
                result.failures.append((item.step_id, FailureSeverity.CRITICAL, error_msg))
                result.unmet_dependencies.add(req)
                logger.warning(error_msg)
            else:
                # Dependency exists, perform deeper checks (e.g., Version compatibility)
                cap = available_caps[req]
                
                # Example Logic: Check if SKILL version is deprecated
                if cap.type == ResourceType.SOFTWARE_LIBRARY and cap.metadata.get("deprecated"):
                    warn_msg = f"Dependency '{req}' is deprecated. Step {item.step_id} may fail soon."
                    result.failures.append((item.step_id, FailureSeverity.WARNING, warn_msg))
                    logger.warning(warn_msg)

    logger.info(f"Validation complete. Executable: {result.is_executable}")
    return result

def simulate_execution_path(result: ValidationResult) -> Dict[str, str]:
    """
    [Core Function 2]
    Generates a simulation trace or execution plan based on the validation result.
    
    If the checklist is deemed executable, this function outlines the execution flow.
    If not, it provides a remediation plan.
    
    Args:
        result (ValidationResult): The result from the validation phase.
        
    Returns:
        Dict[str, str]: A structured execution plan or error report.
    """
    logger.info("Generating execution simulation path...")
    
    response = {
        "status": "READY" if result.is_executable else "BLOCKED",
        "analysis": "",
        "remediation_required": list(result.unmet_dependencies)
    }
    
    if not result.is_executable:
        response["analysis"] = (
            "The cross-domain practice checklist cannot be executed. "
            f"Detected {len(result.unmet_dependencies)} missing core dependencies. "
            "The system lacks the necessary physical or logical primitives "
            "to mimic the source domain behavior."
        )
        # Logic to suggest alternatives could go here
        for failure in result.failures:
            if failure[1] == FailureSeverity.CRITICAL:
                logger.error(f"Critical failure in step {failure[0]}: {failure[2]}")
    else:
        response["analysis"] = (
            "All physical and logical constraints satisfied. "
            "The metaphor mapping is valid within current hardware constraints. "
            "Proceeding to execution phase."
        )
        
    return response

# --- Data Processing Utilities ---

def load_system_profile_from_dict(data: Dict) -> List[SystemCapability]:
    """
    [Utility/Adapter Function]
    Converts a raw dictionary (e.g., from JSON config) into SystemCapability objects.
    
    Args:
        data (Dict): Raw data containing system specs.
        
    Returns:
        List[SystemCapability]: Structured capability list.
    """
    capabilities = []
    for item in data.get("capabilities", []):
        try:
            cap = SystemCapability(
                name=item.get("name"),
                type=ResourceType(item.get("type")),
                version=item.get("version"),
                metadata=item.get("metadata", {})
            )
            capabilities.append(cap)
        except ValueError as e:
            logger.error(f"Invalid resource type in profile data: {e}")
            continue
    return capabilities

