    # The definition is small, but it explains a large amount of repeated data.
    pattern_data = b"abcdefgh" * 10  # 80 bytes
    simple_definition = b"repeat('abcdefgh', 10)"  # Small definition
    
    # Create phenomena that this node explains
    # We observe the sequence 'abcdefgh' multiple times
    observed_phenomena = [
        Phenomenon(id="obs_1", data=pattern_data, occurrences=5), # Seen 5 times
        Phenomenon(id="obs_2", data=pattern_data, occurrences=2)  # Seen 2 times
    ]

    valid_node = InductiveNode(
        id="concept_sequence_abc",
        definition=simple_definition,
        covered_phenomena=observed_phenomena,
        complexity_penalty=20.0 # Arbitrary penalty for the 'concept' overhead
    )

    print("-" * 60)
    is_valid = detect_overfitting(valid_node, ratio_threshold=1.5)
    print(summarize_validation(valid_node, is_valid))

    # Example 2: Overfitting (Noise as Rule)
    # We try to create a rule for random data. The definition must contain the data itself.
    random_data = b"\x13\x37\xbe\xef\xca\xfe" # Random bytes
    # To 'explain' this, the definition is basically the data itself (plus overhead)
    complex_definition = b"store_raw(" + random_data + b")"
    
    noise_phenomena = [
        Phenomenon(id="noise_1", data=random_data, occurrences=1) # Only seen once
    ]

    overfitted_node = InductiveNode(
        id="concept_random_blob",
        definition=complex_definition,
        covered_phenomena=noise_phenomena,
        complexity_penalty=10.0
    )

    print("-" * 60)
    is_valid_noise = detect_overfitting(overfitted_node, ratio_threshold=1.5)
    print(summarize_validation(overfitted_node, is_valid_noise))
