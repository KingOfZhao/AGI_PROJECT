# --- Constants and Enums ---

class TestSeverity(Enum):
    """Severity levels for the sandbox tests."""
    NORMAL = 1
    EDGE_CASE = 2
    ADVERSARIAL = 3
    HALLUCINATION_TRAP = 4

class ValidationStatus(Enum):
    """Result status of the sandbox validation."""
    PASSED = "PASSED"
    FAILED_RUNTIME = "FAILED_RUNTIME"
    FAILED_LOGIC = "FAILED_LOGIC"
    FAILED_SECURITY = "FAILED_SECURITY"
    FAILED_HALLUCINATION = "FAILED_HALLUCINATION"

@dataclass
class TestCase:
    """Represents a single test case generated by the Red Team."""
    case_id: str
    severity: TestSeverity
    inputs: Dict[str, Any]
    expected_behavior: str  # Description of expected behavior (e.g., 'raise ValueError', 'return 0')

@dataclass
class SandboxResult:
    """Result of the sandbox execution."""
    status: ValidationStatus
    score: float
    details: str
    execution_time_ms: float
    logs: List[str] = field(default_factory=list)

# --- Helper Functions ---

def validate_input_schema(inputs: Dict[str, Any], schema: Dict[str, type]) -> bool:
    """
    Helper function to validate input data against a required schema.
    
    Args:
        inputs (Dict[str, Any]): The input dictionary to check.
        schema (Dict[str, type]): A dictionary mapping keys to expected types.
        
    Returns:
        bool: True if valid, False otherwise.
    """
    if not isinstance(inputs, dict):
        return False
    
    for key, expected_type in schema.items():
        if key not in inputs:
            logger.error(f"Missing key in input: {key}")
            return False
        if not isinstance(inputs[key], expected_type):
            logger.error(f"Type mismatch for {key}: Expected {expected_type}, got {type(inputs[key])}")
            return False
    return True

