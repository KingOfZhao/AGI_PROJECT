#!/usr/bin/env python3
"""
sage_council.py — 伟人议会: 三组伟人分工推演
==============================================
发现组(洞察者): 发现问题的本质和隐藏结构
拆解组(工程师): 细化问题为可执行步骤
校验组(审判官): 验证方案是否高效正确

每组调用不同伟人的视角,形成完整的认知闭环。
"""

# ── 三组伟人及其核心视角 ──

# 发现组: 擅长洞察问题本质的伟人
DISCOVERERS = {
    "老子": "道可道非常道——看到表象背后的本质结构",
    "释迦牟尼": "缘起性空——一切现象都有因果链",
    "苏格拉底": "我唯一知道的就是我什么都不知道——追问到底",
    "爱因斯坦": "想象力比知识重要——思想实验发现新框架",
    "毛泽东": "抓主要矛盾——在复杂中找到关键",
    "弗洛伊德": "潜意识决定行为——不知比知更强大",
    "哥白尼": "当现有框架解释不了就换框架",
    "达尔文": "物竞天择——变异+选择=进化",
}

# 拆解组: 擅长将问题细化为可执行步骤的伟人
ENGINEERS = {
    "诸葛亮": "在信息不完整时构建完整战略框架",
    "图灵": "一切可计算问题都能形式化",
    "冯诺依曼": "数据和指令用同一方式存储——万物可编码",
    "钱学森": "整体大于部分之和——系统工程",
    "欧几里得": "从5条公理推导一切——公理化方法",
    "韩非子": "法术势三位一体——用制度而非人治",
    "乔布斯": "简洁是复杂的最终形式",
    "王阳明": "知行合一——知道就要做到",
}

# 校验组: 擅长验证和纠错的伟人
VALIDATORS = {
    "哥德尔": "任何足够强的系统都有无法证明的真命题——找到边界",
    "费曼": "如果你不能简单解释它你就没真正理解——简洁性检验",
    "居里夫人": "在未知中反复实验直到变成已知——实验验证",
    "孙子": "知己知彼百战不殆——对手视角检验",
    "海森堡": "观测本身改变被观测对象——注意观察者偏差",
    "司马懿": "善忍者善胜——时间维度检验",
    "甘地": "以不变原则应万变——原则一致性检验",
    "香农": "信息的本质是消除不确定性——信息量检验",
}


def build_discover_prompt(question, context=""):
    """发现组: 多位洞察者并行审视问题"""
    sages = list(DISCOVERERS.items())[:5]
    prompt = (
        f"你是AGI认知议会的【发现组】。\n"
        f"以下伟人同时审视这个问题,各自给出洞察:\n\n"
    )
    for name, view in sages:
        prompt += f"- {name}视角({view})\n"
    prompt += (
        f"\n问题:\n{question}\n"
        + (f"\n背景:\n{context[:800]}\n" if context else "")
        + "\n请以每位伟人的视角分别给出洞察(每人2-3句):\n"
        "1. 这个问题的真正本质是什么?(不是表面)\n"
        "2. 有什么隐藏的结构/矛盾/假设?\n"
        "3. 最容易被忽略的关键点是什么?\n"
        "\n最后综合: 发现组共识——问题的核心在于____"
    )
    return prompt


def build_engineer_prompt(question, discoveries, context=""):
    """拆解组: 工程师们将洞察转化为可执行方案"""
    sages = list(ENGINEERS.items())[:5]
    prompt = (
        f"你是AGI认知议会的【拆解组】。\n"
        f"发现组已给出洞察:\n{discoveries[:1000]}\n\n"
        f"以下伟人负责细化为可执行方案:\n\n"
    )
    for name, view in sages:
        prompt += f"- {name}视角({view})\n"
    prompt += (
        f"\n原始问题:\n{question}\n"
        + (f"\n背景:\n{context[:500]}\n" if context else "")
        + "\n请拆解为具体可执行步骤:\n"
        "1. 已知(F): 确定的约束和资源\n"
        "2. 未知(V): 需要探索的变量\n"
        "3. 步骤: 每步不超过1小时可完成\n"
        "4. 依赖: 哪些步骤必须先做\n"
        "5. 风险: 每步最可能出错的地方\n"
        "\n如果某部分无法拆解,用 [STUCK] 标记"
    )
    return prompt


def build_validate_prompt(question, plan, context=""):
    """校验组: 审判官们验证方案质量"""
    sages = list(VALIDATORS.items())[:5]
    prompt = (
        f"你是AGI认知议会的【校验组】。\n"
        f"拆解组已给出执行方案:\n{plan[:1200]}\n\n"
        f"以下伟人负责严格校验:\n\n"
    )
    for name, view in sages:
        prompt += f"- {name}视角({view})\n"
    prompt += (
        f"\n原始问题:\n{question}\n"
        + (f"\n背景:\n{context[:500]}\n" if context else "")
        + "\n请逐项校验:\n"
        "1. 逻辑完整性: 步骤能否达成目标?(哥德尔)\n"
        "2. 简洁性: 有没有多余步骤?(费曼)\n"
        "3. 可验证性: 每步怎么知道做对了?(居里夫人)\n"
        "4. 对手视角: 如果想让这个方案失败会怎么做?(孙子)\n"
        "5. 时间维度: 短期好但长期会出问题吗?(司马懿)\n"
        "\n最终裁决: [通过] 或 [驳回:原因] 或 [修正:具体修改]"
    )
    return prompt
